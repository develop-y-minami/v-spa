/**
 * APIレスポンスの共通型定義
 * 
 * このファイルでは、すべてのAPIレスポンスに共通する型を定義します。
 * 各APIリクエストのレスポンスが同じ構造を持つことを保証し、型安全にします。
 * 
 * @template T - レスポンスデータの型。APIのレスポンスデータ部分は、T で定義されます。
 */
export interface ApiResponse<T> {
    /**
     * リクエストが成功したかどうかを示すフラグ
     * - `true` ならリクエストが成功
     * - `false` ならリクエストが失敗
     */
    success: boolean;
  
    /**
     * レスポンスメッセージ
     * リクエストの結果に関するメッセージが格納されます。
     * 成功時は「Request was successful」などが入り、失敗時はエラーメッセージが入ります。
     */
    message: string;
  
    /**
     * レスポンスデータ
     * - T 型でデータ部分を定義します。
     * - 成功時はデータがこのフィールドに格納され、失敗時は `data` フィールドが空または `null` になる場合があります。
     */
    data: T;
  }
  